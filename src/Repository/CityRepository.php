    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, City::class);
    }

    public function findByName(string $name,string $countryCode) : ?object
    {
        $gb = $this->createQueryBuilder('m');
        $gb->where('m.Name = :name')
            ->setParameter('name',$name)
            ->andWhere("m.CountryCode = :country_code")
            ->setParameter("country_code",$countryCode)
            ->getFirstResult();
        $query = $gb->getQuery();
        return $query->getResult()[0];
    }

